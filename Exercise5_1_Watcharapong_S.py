firstNumber = input("First Number :")
secondNumber = input("Second Number :")
resultAdd = int(firstNumber) + int(secondNumber)
resultMinus = int(firstNumber) - int(secondNumber)
resultMultiply = int(firstNumber) * int(secondNumber)
resultDivide = int(firstNumber) / int(secondNumber)
print(firstNumber, "+", secondNumber, "=", resultAdd)
print(firstNumber, "-", secondNumber, "=", resultMinus)
print(firstNumber, "*", secondNumber, "=", resultMultiply)
print(firstNumber, "/", secondNumber, "=", resultDivide)
