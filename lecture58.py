# String Format Example

name = "Wach"
weight = "66"
print("Hello" + name + " ! and weight is " + weight + "kg")
print("Hello %s ! and weight is %s kg" % (name, weight))
print("Hello %s ! and weight is %d kg" % (name, 66))
print("Hello %s ! and weight is %d kg" % (name, 66.66))
print("Hello %s ! and weight is %f kg" % (name, 66.66))
